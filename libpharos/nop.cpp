bool insn_is_nop(const SgAsmX86Instruction* insn, const DescriptorSet& ds) {
  // Instructions explicitly cataegorized as NOP instructions, are obviously NOPs.  Even if the
  // instruction has a sizeable list of bogus prefixes, this mnemonic test appears to do the
  // correct thing.
  if (insn->get_kind() == x86_nop) {
    return true;
  }

  // Other tests are based on the bytes of the instruction.
  // Make a copy, since we'll be removing some prefixes before searching.
  const SgUnsignedCharList bytes = insn->get_rawBytes();
  size_t size = bytes.size();
  // Empty or malformed instructions are not NOPs.
  if (size < 1) return false;

  // Some NOPs are independent of the architecture bit width.
  if (nop_instructions.find(bytes) != nop_instructions.end()) {
    return true;
  }

  // Other NOP patterns are dependent on the architecture bit width, largely due to the way
  // that 64-bit platforms zero out the top half of 64-bit registers on 32-bit operations.
  size_t bits = ds.get_arch_bits();
  if (bits == 32) {
    return x86_nop_instructions.find(bytes) != x86_nop_instructions.end();
  }
  else {
    return x64_nop_instructions.find(bytes) != x64_nop_instructions.end();
  }
}

// Return True if the block is an alignment code block.  By this we really mean that the block
// has no known predecessor (is probably not really in the control flow), and is composed
// entirely of NOP/INT3 instructions.  Typically, this block will have a legitimate successor,
// because the NOPs will flow into the block following this one.  Because the question of
// whether the block has a predecessor can perform poorly right now, we're going to fudge this
// right now, and only implement the test for whether the block is composed entirely of NOP
// instructions.  Presumably the number of false positives will be so small as to make the
// difference negligible.  Longer term, it would be nice if these blocks were fully transformed
// into alignment data blocks in the partitioner, but there's some disagreement with the ROSE
// partitioner about how to handle these.
bool block_is_align(
  SgAsmBlock* bb,
  const DescriptorSet& ds,
  const Rose::BinaryAnalysis::NoOperation& nop_detector)
{
  // Iterate through instructions, returning false on the first non-NOP instruction.
  SgAsmStatementPtrList & insns = bb->get_statementList();
  for (size_t y = 0; y < insns.size(); y++) {
    // Handle X86 instructions in a custom (high-performance) way using insn_is_nop() above.
    SgAsmX86Instruction *insn = isSgAsmX86Instruction(insns[y]);
    SgAsmInstruction *ginsn = isSgAsmInstruction(insns[y]);
    if (!ginsn) continue;
    if (insn != NULL) {
      // INT3 instructions are commonly used as padding.  If we were properly testing whether
      // the block was in the control flow, we should also allow INT3 instructions, which is
      // another common instruction for compilers to fill alignment bytes with.
      // if (insn->get_kind() == x86_int3) continue;

      bool our_is_nop = insn_is_nop(insn, ds);
      //bool rose_is_nop = SageInterface::isNOP(ginsn);
      bool rose_is_nop = nop_detector.isNoop(ginsn);
      // If this instruction is NOT a NOP, the block is NOT a NOP block.
      if (our_is_nop != rose_is_nop && insn->get_kind() != x86_jmp) {
        OERROR << "NOP disagreement pharos=" << our_is_nop << " rose=" << rose_is_nop << " "
               << debug_instruction(insn) << LEND;
      }
      if (!our_is_nop) return false;
    }
    // For all non-X86 instructions, fall back to the ROSE NOP detector (might perform poorly).
    else {
      return nop_detector.isNoop(ginsn);
    }
  }
  if (insns.size() == 0) {
    OINFO << "Block " << std::hex << bb->get_address() << std::dec << " is an empty block!" << LEND;
  }
  else {
    OINFO << "Block " << std::hex << bb->get_address() << std::dec << " is an alignment block!" << LEND;
  }
  return true;
}

